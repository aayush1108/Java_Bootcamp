package whileLoops.Demo;

public class WhileLoopDemo {
    public static void main(String[] args){
        int i=2;
        int j=2;
        while(i<=10){
            System.out.println(i);
            i++;
        }
        do {
            System.out.println(j);
            j++;
        }
        while(j<=10);
    }
}
